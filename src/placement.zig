fn getRandomNodeID(
    the_placement: *const Placement,
    random: std.Random,
) ?glib.NodeId {
    const count = the_placement.locations.count();
    if (count == 0) return null;

    const index = random.intRangeLessThan(usize, 0, count);
    return the_placement.locations.keys()[index];
}

// randomly perturbs the placement
fn perturb(the_graph: *const Graph, the_placement: *Placement, random: std.Random, current_window_size: f32, annealing_config: AnnealingConfig) void {
    errdefer @panic("Ran out of memory lol");
    for (0..annealing_config.perturbations_amount) |_| {
        const to_perturb = getRandomNodeID(the_placement, random).?;
        const result = move(the_graph, the_placement, to_perturb, @intFromFloat(@ceil(current_window_size)), random, annealing_config.grid_size, annealing_config.grid_size); // use grid size as the minimum y position, so it stays nicely alligned if we changed it in the future
        if (result != 0) {
            _ = swap(the_graph, the_placement, to_perturb, result);
        }
        // TODO: add mirror or other perturbations
    }
}

// assigns a position to each cell in the graph, using the annealing placement algorithm.
// pseudo code:

// Simulated Annealing Algorithm for Placement
// Input: set of all cells V
// Output: placement P
// 1 T=T0
// 2 P=PLACE(V)
// 3 while(T>Tmin)
// 4 while(!STOP())
// new_P =PERTURB(P)
// Δcost =COST(new_P)–COST(P)
// if (Δcost <0)
// P =new_P
// else
// r =RANDOM(0,1)
// 11 if(r<e^(-Δcost/T))
// P =new_P
// T=α∙T

pub fn placement_annealing(the_graph: *const Graph, annealing_config: AnnealingConfig) ?*Placement {
    errdefer @panic("Skill issue");

    var current_placement = initialPlacement(the_graph, annealing_config.initial_spacing, annealing_config.initial_row_size, annealing_config.grid_size);
    print(the_graph, current_placement, the_graph.gpa);
    var temperature = annealing_config.initial_temperature;
    var current_window_size = annealing_config.initial_window_size;

    // get random generator:
    var seed: u32 = undefined;
    std.posix.getrandom(std.mem.asBytes(&seed)) catch |err| {
        std.debug.print("Failed to get random seed: {}\n", .{err});
        return null;
    };
    var prng = std.Random.DefaultPrng.init(seed);
    const rand = prng.random();

    // should be in (0,1)
    // should be a lower value at the start and end of the simulation, and a higher value in the middle, during refining.
    // it is constant for now.
    const alpha = 0.95;
    const actual_moves_per_temperature = @divFloor(annealing_config.moves_per_temperature, annealing_config.perturbations_amount);

    while (temperature > annealing_config.minimum_temperature) {
        var lastCost: f32 = std.math.floatMax(f32);
        for (0..actual_moves_per_temperature) |i| {
            var new_placement = try current_placement.clone(the_graph.gpa);

            perturb(the_graph, new_placement, rand, current_window_size, annealing_config);
            const current_cost = cost(the_graph, current_placement, annealing_config);
            const new_cost = cost(the_graph, new_placement, annealing_config);
            const cost_diff = new_cost - current_cost;
            if (cost_diff < 0) {
                current_placement.deinit(the_graph.gpa);
                current_placement = new_placement;
                lastCost = new_cost;
            } else {
                const value = rand.float(f32);
                // compute e^(-Δcost/T):
                const exponent = (-cost_diff / temperature);
                if (value < std.math.exp(exponent)) {
                    current_placement.deinit(the_graph.gpa);
                    current_placement = new_placement;
                    lastCost = new_cost;
                } else {
                    new_placement.deinit(the_graph.gpa);
                    lastCost = current_cost;
                }
            }
            const progress = (@as(f32, @floatFromInt(i)) / @as(f32, @floatFromInt(actual_moves_per_temperature))) * 100;
            if (actual_moves_per_temperature > 100) {
                // print every 10%
                if (i % (actual_moves_per_temperature / 10) == 0) {
                    std.log.info(
                        "{d}% progress of Temp: {d}, Cost: {d}",
                        .{ progress, temperature, lastCost },
                    );
                }
            }
        } // exit loop if in equilibrium at this temperature

        print(the_graph, current_placement, the_graph.gpa);

        std.log.info(
            "Cost: {d}, Temp: {d}, windowsize: {d}\n",
            .{ lastCost, temperature, current_window_size },
        );
        // compute wnext=wcurr*(log(tnext)/log(tcurr)):
        current_window_size = current_window_size * (std.math.log10(temperature * alpha) / std.math.log10(temperature));
        // compute tnext:
        temperature *= alpha;
    }
    return current_placement;
}
